def write_seed() -> int:
    SEED_PATH.parent.mkdir(parents=True, exist_ok=True)
    with SEED_PATH.open("w", encoding="utf-8") as out:
        for index, text in enumerate(SEED_SENTENCES):
            record = {
                "source_site": "synthetic-seed",
                "post_url": f"seed://ajae/{index:03d}",
                "content_type": "post",
                "text": text,
                "text_length": len(text),
                "pii_masked": [],
                "safety_flags": [],
            }
            out.write(json.dumps(record, ensure_ascii=False) + "\n")
    # 정제 버전도 동일하게 둔다(시드는 이미 안전 정제 상태)
    CLEAN_PATH.parent.mkdir(parents=True, exist_ok=True)
    CLEAN_PATH.write_text(SEED_PATH.read_text(encoding="utf-8"), encoding="utf-8")
    return len(SEED_SENTENCES)


def main() -> None:
    n = write_seed()
    print(f"[1/3] 시드 {n}건 작성: {SEED_PATH}")
    imported = import_clean_jsonl([str(SEED_PATH)])
    print(f"[2/3] DB 적재: {imported}건")
    model = train_style_model()
    print(f"[3/3] 문체 학습 완료: {model['document_count']}건 -> analysis/style_model.json, analysis/style_report.md")


if __name__ == "__main__":
    main()
